




function crew1() {

    var buttonsCrew = document.querySelector('.button-crew')
    var changeCrew = document.querySelector('.douglas')
    var changeCommander = document.querySelector('.comander')
    var changeName = document.querySelector('.name')
    var changeHistory = document.querySelector('.history')

    changeCrew.setAttribute('src', 'image-douglas-hurley.png')
    changeCommander.innerHTML = ' COMMANDER '
    changeName.innerHTML = 'DOUGLAS HURLEY'
    changeHistory.innerHTML = 'Douglas Gerald Hurley is an American engineer, former Marine Corps pilot and former NASA astronaut. He launched into space for the third time as commander of Crew Dragon Demo-2.'

}


